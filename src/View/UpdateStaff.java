    private void btn_SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SaveActionPerformed
        // TODO add your handling code here:
        int state = 0;
        if(jCB_Dismissal.isSelected()) {
            state = 1;
        }
        HRManagerModel hRManagerModel = new HRManagerModel();
        HrManagerController hrManagerController = new HrManagerController(hRManagerModel);
        Staff staff = new Staff();
        staff.setiD(HrManager.IdStaff);
        staff.setFirstName(lbl__FirstName.getText());
        staff.setLastName(lbl__LastName.getText());
        Date selectDate = jDC_DOB.getDate();
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");
        String dates = dateFormat.format(selectDate);
        java.sql.Date sqlDate = new java.sql.Date(selectDate.getTime());
        staff.setDob(sqlDate);
        staff.setGender(cbb__Gender.getSelectedItem().toString());
        staff.setPhoneNumber(Integer.parseInt(lbl__PhoneNumber.getText()));
        staff.setEmail(lbl__UserName1.getText());
        Date HireDate = jDC_HireDate.getDate();
        java.sql.Date HireDateSql = new java.sql.Date(HireDate.getTime());
        staff.setHireDate(HireDateSql);
        staff.setPosition(cbb__Position.getSelectedItem().toString());
        staff.setSalary(Double.parseDouble(lbl__Salary.getText()));
        if(hrManagerController.UpdateStaff(staff)) {
            JOptionPane.showMessageDialog(this, "Sửa nhân viên thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            HrManager hrManager = new HrManager();
            hrManager.setVisible(true);
            this.setVisible(false);
        }
        else {
            JOptionPane.showMessageDialog(this, "Sửa nhân viên thất bại", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_SaveActionPerformed

